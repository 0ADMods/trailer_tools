function executeGatherCommands(commands) {
	for (const command of commands)
		ProcessCommand(1, {
			"type": "gather",
			"entities": command.entities,
			"target": command.target,
			"queued": false,
			"pushFront": false
		});
}

function executeWalkCommands(commands) {
	for (const command of commands)
		ProcessCommand(1, {
			"type": "walk",
			"entities": command.entities,
			"x": command.x,
			"z": command.z,
			"queued": command.queued,
			"pushFront": false
		});
}

